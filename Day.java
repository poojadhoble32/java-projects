package dayFind;

public class Day {
	String findDay(int iNo)
    {
	  switch(iNo)
	  {
	  case 1: return "Sunday";
	  //break;
	  case 2: return "Monday";
	  //break;
	  case 3: return "tuesday";
	  //break;
	  case 4: return "thursday";
	  //break;
	  case 5: return "friday";
	  //break;
	  case 6: return "Saturday";
	  //break;
	  default: return "invalide input";
	  //break;
	  
	  }
    }

}
